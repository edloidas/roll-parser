module.exports = {
  simple: [
    /^(\d+)$/,                                                                 // 20
    /^(\d+)(?:\s+)(\d+)$/,                                                     // 1 20
    /^(\d+)(?:\s+)(\d+)(?:\s+)([+-]?\d+)?$/,                                   // 1 20 +4
    /^(\d+)(?:\s+)(\d+)(?:\s+)([+-]?\d+)(?:\s+)(\d+)?$/,                       // 1 20 +4 5
    /^(\d+)(?:\s+)(\d+)(?:\s+)([+-]?\d+)(?:\s+)(\d+)(?:\s+)(\d+)?$/,           // 1 20 +4 5 20
  ],
  classic: [
    /^(?:[dD])(\d+)$/,                                                         // d20
    /^(\d*)(?:[dD])(\d+)$/,                                                    // 1d20
    /^(\d*)(?:[dD])(\d+)([+-]\d+)$/,                                           // 1d20+4
    /^(\d*)(?:[dD])(\d+)(?:\s+\(\s*)(\d+)(?:\s*,?\s*\))$/,                     // 1d20 (5) || 1d20+4 (5,)
    /^(\d*)(?:[dD])(\d+)(?:\s+\(\s*)(\d*)(?:\s*,\s*)(\d+)(?:\s*\))$/,          // 1d20 (5,18) && 1d20+4 (,18)
    /^(\d*)(?:[dD])(\d+)([+-]\d+)(?:\s+\(\s*)(\d+)(?:\s*,?\s*\))$/,            // 1d20+4 (5) || 1d20+4 (5,)
    /^(\d*)(?:[dD])(\d+)([+-]\d+)(?:\s+\(\s*)(\d*)(?:\s*,\s*)(\d+)(?:\s*\))$/, // 1d20+4 (5,18) && 1d20+4 (,18)
  ],
};
